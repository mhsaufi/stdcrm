<h5>Perkahwinan Dalam Islam Dan Hukum-Hukumnya</h5>
<p>Dan jika kamu takut tidak berlaku adil terhadap perempuan-perempuan yatim (apabila kamu berkahwin dengan mereka), maka kahwinlah dengan sesiapa yang kamu berkenan dari perempuan-perempuan (lain): dua, tiga atau empat. Kemudian jika kamu bimbang tidak akan berlaku adil (di antara isteri-isteri kamu) maka (berkahwinlah dengan) seorang sahaja, atau (pakailah) hamba-hamba perempuan yang kamu miliki. Yang demikian itu adalah lebih dekat (untuk mencegah) supaya kamu tidak melakukan kezaliman.
<br><br><small>(Surah An-Nisa, 4:3)</small></p>
<br>
<p>Perkahwinan bermaksud sebuah ikatan antara lelaki dan wanita melalui akad nikah dan ijab kabul. Jika seorang lelaki dan seorang wanita saling mencintai dan telah mendapat restu daripada kedua-dua belah pihak serta sekiranya keduanya mampu, maka mereka boleh melangsungkan perkahwinan. </p>
<br>
<p>Apa itu nikah? Dari segi bahasa, ia bermaksud ikatan atau simpulan manakala dari segi istilah syarak ia merupakan sebuah ikatan atau akad yang menghalalkan pergaulan dan membatas hak dan kewajipan serta tolong-menolong antara seorang lelaki dan seorang perempuan yang bukan mahram.

Rasulullah SAW juga telah bersabda yang bermaksud: </p>

<br>

<p>Adalah Nabi SAW memerintahkan kami untuk berkahwin dan menegah kami daripada membujang dengan tegahan yang tegas. Dan sabda baginda: Kahwinlah kamu dan beranak pinaklah kamu kerana sesungguhnya aku berbangga kerana banyak bilangan kamu pada hari kiamat kelak.
<br><br><small>(Hadis Riwayat Imam Ahmad)</small></p>

<br>

<p>Sesebuah perkahwinan akan menjadi sah apabila lengkap rukun nikah, iaitu lelaki atau calon suami, perempuan atau calon isteri, wali, dua orang saksi lelaki dan sighah, iaitu ijab dan kabul. </p>
<br>

<h6>#1 – Bakal Suami</h6>
<br>
<p>Bakal suami perlulah seorang lelaki yang Islam serta bukan mahram dengan bakal isteri. Orang yang sedang dalam ihram haji atau umrah tidak boleh melaksanakan perkahwinan. Ia juga perlu di atas kerelaan sendiri dan tidak sah sesebuah perkahwinan tersebut jika terdapat paksaan.

Lelaki yang ingin berkahwin itu mestilah seorang yang bujang, atau jika telah berkahwin, dia tidak boleh mempunyai empat orang isteri. Dalm erti kata lain, kuotanya masih ada. Hal ini kerana, lelaki hanya boleh berkahwin empat sahaja dalam satu masa, jika lebih, maka ia adalah tidak dibolehkan.</p>

<h6>#2 – Bakal Isteri</h6>
<p>Bakal isteri perlulah seorang perempuan yang Islam dan bukan khunsa. Perempuan tersebut tidak boleh berada dalam keadaan idah dan ihram haji atau umrah. Dia juga bukanlah perempuan mahram dengan bakal suami serta bukan isteri orang. </p>

<h6>#3 – Wali</h6>

<p>Syarat sah untuk menjadi wali adalah seorang yang Islam dan adil serta baligh dan perlulah lelaki yang merdeka. Wali tersebut tidak boleh berada dalam keadaan ihram atau umrah dan tidak cacat akal fikiran atau gila serta tidak fasik, kafir atau murtad.

لا نكا ح ا لا بو لى وشا هد ى عد ل وما كا ن من نكا ح غير ذ لك فهو با طل

Mafhum: Tidak sah suatu pernikahan tanpa seorang wali dan dua orang saksi yang adil. Dan pernikahan yang mana pun yang tidak demikian adalah batal. (Hadis Riwayat Bukhari)

Terdapat empat jenis wali, iaitu wali mujbir, wali aqrab, wali ab’ad dan wali hakim.

Wali mujbir merupakan wali dari bapa sendiri atau datuk sebelah bapa. Mereka mempunyai kuasa untuk mewalikan perkahwinan anak perempuannya atau cucu perempuannya dengan atau tanpa persetujuan.

Wali aqrab pula merupakan wali yang terdekat mengikut susunan yang layak dan berhak menjadi wali. Contohnya selepas susunan ayah dan datuk, abang atau adik lelaki perempuan tersebut yang boleh menjadi wali.

Wali ab’ad pula merupakan wali yang jauh sedikit mengikut susunan yang layak menjadi wali jika tiada wali aqrab. Sebagai contoh anak saudara lelaki.

Akhir sekali, jika tiada wali maka dibolehkan untuk menggunakan wali hakim. Ia merupakan sultan atau raja yang bertindak sebagai wali kepada pengantin perempuan yang tidak mempunyai wali. Wali Hakim ditakrifkan sebagai wali yang ditauliahkan oleh Yang Dipertuan Agong dalam hal Wilayah Persekutuan, Pulau Pinang, Sabah dan Sarawak atau Raja dalam hal sesuatu negeri lain untuk mengahwinkan perempuan yang tidak mempunyai wali nasab. Ia disebut melalui Akta Undang-Undang Keluarga Islam Wilayah Persekutuan, Seksyen (2). </p>