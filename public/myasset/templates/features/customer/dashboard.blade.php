<h3>Dashboard</h3>

<p>Once your booking has successfully accepted, you will be allowed to access your event <span class="nav_link_text">Dashboard</span>. Here, we will walk you through the features of our dashboard so that you can freely navigate around.</p>
<br><br>
<h5>Event Banner</h5>


<div style="width: 100%;margin: 20px 0;">
	<img src="myasset/features/start_planning/5.JPG">
</div>

<p>This is your wedding event information banner. It is just an information board for everyone within this event dashboard to take notice. Please take note that, you are not the only person seeing this information board, the company and related vendors are seeing the same thing as if you are all within a WhatsApp group.</p>

<p>If you take a look at the top-right of the banner, you will see the 3-dots. If you click on it, a little popup will appear with a list of option either to <span class="nav_link_text">Edit Information</span> or <span class="nav_link_text">Cancel Event</span>.</p>
<br><br>
<h5>Edit Information</h5>
<p>Nothing much other than just edit your event information and update it.</p>

<br><br>
<h5>Cancel Event</h5>
<p>This is considered as a rare occasion where people cancels their wedding event. This might be due to several unavoided reasons. Anyway, if you choose to cancel your event, there are several things to be noted. We will talk about this later in this tutorial</p>
<br><br>
<h5>Timeline Notification</h5>
<br>
<div class="first">
		<p><img src="myasset/features/start_planning/6.JPG" align="left"> This is your timeline notification section. Notification cards appearing here are the timelines created by everyone within this event dashboard. Take note that, it is our platform policy where anyone within this event dashboard cannot simply creates their timeline.<br><br> Timeline creation will need approval from someone assigned to within the timeline. If A is creating a timeline to label his meeting with B, A will has to assign B as an <span class="nav_link_text">Assigned To</span> person for this timeline. Therefore, for this timeline to be created, A will need to wait for B to approve this timeline creation. In this scenario, a notification card will appear within B's dashboard with a <span class="nav_link_text">yellow coloured</span> header indicating that this timeline is created by someone within this event and need your verfification and approval. However, B will has option either to <span class="nav_link_text">Approve</span> this timeline or <span class="nav_link_text">Disapprove</span>. If it is disapproved, A will see the <span class="nav_link_text">red coloured</span> notification card indicating that A need to <span class="nav_link_text">update</span> the timeline to make sure B satisfy or simply <span class="nav_link_text">delete</span> and create new timeline</p>
</div>

