<h3>Dashboard</h3>

<p>Once your booking has successfully accepted, you will be allowed to access your event <span class="nav_link_text">Dashboard</span>. Here, we will walk you through the features of our dashboard so that you can freely navigate around.</p>
<br><br>
<h5>Event Banner</h5>


<div style="width: 100%;margin: 20px 0;">
	<img src="myasset/features/start_planning/5.JPG">
</div>

<p>This is your wedding event information banner. It is just an information board for everyone within this event dashboard to take notice. Please take note that, you are not the only person seeing this information board, the company and related vendors are seeing the same thing as if you are all within a WhatsApp group.</p>

<p>If you take a look at the top-right of the banner, you will see the 3-dots. If you click on it, a little popup will appear with a list of option either to <span class="nav_link_text">Edit Information</span> or <span class="nav_link_text">Cancel Event</span>.</p>
<br><br>
<h5>Edit Information</h5>
<p>Nothing much other than just edit your event information and update it.</p>

<br><br>
<h5>Cancel Event</h5>
<p>This is considered as a rare occasion where people cancels their wedding event. This might be due to several unavoided reasons. Anyway, if you choose to cancel your event, there are several things to be noted. We will talk about this later in this tutorial</p>
