<p>Please do remember, after you complete submitting your booking form, you will need to wait for the company to accept your booking before you can navigate to the <span class="nav_link_text">Dashboard</span> and organize everything there. Before that, if you jump to your <span class="nav_link_text">Dashboard</span>, you will just see your booking information. It is good to always login and check your booking status, but we will take responsibility to inform you through your registered email address once your booking is accepted by the company.</p>

<h6>What to do if my booking is rejected?</h6>

<p>There are cases where people playing around and do booking just for fun. These will results to dificulties for the companies and vendors by filling up their work bucket with fake deals. Therefore, this is our initiatives to avoid that issue. So, if your booking is been rejected without accepting any information from the company, this might be because they cannot accept it anymore due to their full work bucket or other unavoided circumstances.</p>

<p>Don't worry, you can browse for other packages and do new booking once you clear your booking cart.</p>
