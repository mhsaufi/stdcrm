<h5>Via Online Booking (recommended)</h5>

<p>Of course, this is the recommended option! Why else do you use our platform if you still had to do everything manually? But there is still some consequences where the second option can be the better option.</p>

<p>If you navigate through the main navigation menu on the top of the page, you can see the <span class="nav_link_text">Vendors</span> and 
	<span class="nav_link_text">Packages</span> link. From either one, you can do your booking.</p>

<p>From the <span class="nav_link_text">Vendors</span> link, you will be brought to view all of registered vendors and companies. Just choose which vendors you feel like the best and view them. From the Company View Page, you will see another navigation menu on the page. Select the <span class="nav_link_text">Packages</span> tab and choose their package</p>

<div style="width: 100%;margin: 50px 0;">
	<img src="myasset/features/start_planning/3.JPG">
</div>

<p>The advantage of viewing the package from the company view is that, you can select from which the highest rating and reviewed company if you are a newbie and doesn't know which is the best planner. As you can navigate to the <span class="nav_link_text">Review</span> section and decide based on people's opinion on this company.</p>

<p>Meanwhile if you directly jump to the <span class="nav_link_text">Packages</span> tab from the menu on the top of the page, you can select the best package available from any company. Click or select package of your choice, fill in the form, and book.</p>

